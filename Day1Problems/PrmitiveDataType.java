package Day1Problems;

public class PrmitiveDataType {
    static boolean val1;
    static int val2;
    static float val3;
    static String val4;
    static long val5;
    static byte val6;
    static char val7;

    public static void main(String[] args) {
        System.out.println("Boolean is " + val1);
        System.out.println("Int value is " + val2);
        System.out.println("Float value is " + val3);
        System.out.println("String value is " + val4);
        System.out.println("Long value is " + val5);
        System.out.println("Byte value is " + val6);
        System.out.println("Char value is " + val7);
    }

}
